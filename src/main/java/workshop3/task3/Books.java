package workshop3.task3;

public class Books {
    public String name;
    public String author;
    public Double price;
    public Integer year;
    public Integer numberOfPages;

    public Books(String name, String author, Double price, Integer year, Integer numberOfPages) {
        this.name = name;
        this.author = author;
        this.price = price;
        this.year = year;
        this.numberOfPages = numberOfPages;
    }
}
